#include "main.h"
/**
 * print_sign - Print sign of a number
 * @n: The integer to check
 * Return: 0 Always success
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n == 0)
	{
		_putchar(48);
		return (0);
	}
	else if (n < 0)
	{
		_putchar('-');
	}
	return (-1);
}
